package patterns;

public class NXNPattern {
    public static void main(String s[]) {
        int n = 100;
        for(int x=0;x<n;x++)
        {
            for(int y=0;y<n;y++)
                System.out.print("*");
                System.out.println("");
        }
        System.out.println("");
        System.out.println("done");
    }
}
